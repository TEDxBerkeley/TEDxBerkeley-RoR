/* Set the width of the side navigation to 250px */
function moveNav() {
  var navBar = document.getElementById("mySideNav");
  if (navBar.style.width != "250px") {
    navBar.style.width = "250px";
  } else {
    navBar.style.width = "0px";
  }
}

